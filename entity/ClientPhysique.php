        $this->login = $login;
    }

    /**
     * @return mixed
     */
    public function getNci()
    {
        return $this->nci;
    }

    /**
     * @param mixed $nci
     */
    public function setNci($nci)
    {
        $this->nci = $nci;
    }

    /**
     * @return mixed
     */
    public function getClMoral()
    {
        return $this->cl_moral;
    }

    /**
     * @param mixed $cl_moral
     */
    public function setClMoral($cl_moral)
    {
        $this->cl_moral = $cl_moral;
    }

    /**
     * @return mixed
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * @param mixed $type
     */
    public function setType($type)
